const ORIGINAL = ART.split('\n');

const DENSITY_SWAP = {
  '@': ['%', '#'],
  '%': ['@', '#'],
  '#': ['%', '*'],
  '*': ['+', '#'],
  '+': ['=', '*'],
  '=': ['+', '-'],
  '-': ['=', ':'],
  ':': ['-', '.'],
  '.': [':', ' '],
};

export function initAsciiArt() {
  const pre = document.querySelector('.ascii-portrait');
  const bg = document.querySelector('.ascii-portrait-bg');
  const terminal = document.getElementById('terminal');
  if (!pre || !bg || !terminal) return;

  const art = ORIGINAL.map(l => l.split(''));
  let revealed = 0;
  let revealTimer = null;
  let mutationTimer = null;
  let dismissed = false;

  function render() {
    pre.textContent = art.slice(0, revealed).map(r => r.join('')).join('\n');
  }

  function revealStep() {
    if (dismissed) return;
    if (revealed >= art.length) {
      startMutation();
      return;
    }
    revealed++;
    render();
    revealTimer = setTimeout(revealStep, 17);
  }

  function mutateOnce() {
    if (dismissed) return;
    const row = Math.floor(Math.random() * art.length);
    const col = Math.floor(Math.random() * art[row].length);
    const orig = ORIGINAL[row]?.[col];
    if (!orig || orig === ' ') return;
    const swaps = DENSITY_SWAP[orig];
    if (!swaps) return;
    art[row][col] = swaps[Math.floor(Math.random() * swaps.length)];
    render();
    setTimeout(() => {
      if (dismissed) return;
      art[row][col] = orig;
      render();
    }, 60 + Math.random() * 140);
  }

  function startMutation() {
    window.dispatchEvent(new CustomEvent('ascii-art-complete'));
    mutationTimer = setInterval(() => {
      const n = 2 + Math.floor(Math.random() * 4);
      for (let i = 0; i < n; i++) {
        setTimeout(mutateOnce, Math.random() * 200);
      }
    }, 280);
  }

  function dismiss() {
    if (dismissed) return;
    dismissed = true;
    clearTimeout(revealTimer);
    clearInterval(mutationTimer);
    bg.style.transition = 'opacity 0.6s ease';
    bg.style.opacity = '0';
    setTimeout(() => { bg.style.display = 'none'; }, 600);
  }

  let started = false;
  function start() {
    if (started) return;
    started = true;
    revealStep();
  }
  window.addEventListener('preloader-complete', () => setTimeout(start, 150), { once: true });
  setTimeout(start, 4000);

  // Dismiss when the user first types (not on any click — avoids accidental dismissal)
  const termInput = document.getElementById('term-input');
  if (termInput) termInput.addEventListener('input', dismiss, { once: true });

  // Auto-dismiss 12s after the art fully appears, so it never blocks indefinitely
  window.addEventListener('ascii-art-complete', () => setTimeout(dismiss, 12000), { once: true });
}
